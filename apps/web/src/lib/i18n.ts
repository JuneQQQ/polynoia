export type TKey = keyof typeof STR;

export function t(key: TKey, lang: Lang): string {
	const entry = STR[key];
	if (!entry) return key as string;
	// Defensive: callers read `lang` from the store; if it's ever undefined
	// (a test store that doesn't seed it, or pre-hydration) fall back to zh
	// instead of rendering `undefined`.
	return entry[lang] ?? entry.zh;
}

const STORAGE_KEY = "polynoia.lang";

export function loadLang(): Lang {
	if (typeof window === "undefined") return "zh";
	const v = window.localStorage.getItem(STORAGE_KEY);
	return v === "en" ? "en" : "zh";
}

export function saveLang(lang: Lang) {
	if (typeof window === "undefined") return;
	window.localStorage.setItem(STORAGE_KEY, lang);
}
