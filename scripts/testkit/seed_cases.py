def sample_attachments(key: str, conv_id: str):
    specs = ATTACHMENTS.get(key)
    if not specs:
        return []
    out = []
    for a in specs:
        t = a["type"]
        if t == "img":
            data, mt = asset_png_bytes(a["asset"]), "image/png"
        elif t == "csv":
            data, mt = sample_csv_bytes(key), "text/csv"
        elif t == "docx":
            data, mt = minimal_docx_bytes("材料初稿", "这是供参考的初稿,具体以正文沟通为准。"), _DOCX_MIME
        elif t == "pdf":
            data, mt = minimal_pdf_bytes(), "application/pdf"
        else:
            continue
        out.append(upload_attachment(conv_id, a["name"], mt, data))
    return out


