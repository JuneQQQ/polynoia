"""Seed realistic, EFFECT-DRIVEN demo cases for AgentHub/Polynoia.

Design principle (per product direction 2026-06-13):
    Real users are *outcome-driven*, not artifact-driven. They know the EFFECT
    they want ("give my kid a little game to play on my phone"), rarely the
    deliverable, the tech stack, or the file layout — and they have never heard
    of internal tools like `present`. The product's job is to help them figure
    it out. So these draft prompts are written in an ordinary user's voice:
      · state the problem / desired outcome, NOT the artifact
      · no tech-stack dictation, no directory layout, no role assignment
      · no internal jargon (present / 工作区 / main / 验收 / 沙箱 / 目录)
      · most are vague or "help me think"; only a few are spec'd (the rare expert)

Each draft is left UNSENT (draft_text) so the tester sends it and watches the
orchestrator do the clarifying + decomposition.

Idempotent: re-running reuses contacts / workspaces / conversations by name/title
and just refreshes the draft. Talks to the live API on :7780 (started by reset.sh).
"""
