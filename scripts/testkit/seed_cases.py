def main() -> int:
    for adapter_id in ("claudeCode", "codex", "opencoder"):
        req(f"/api/agents/{adapter_id}/enable", {})

    existing_agents = {a["name"]: a for a in get("/api/agents")}
    ids = {}
    for contact in TEAM:
        existing = existing_agents.get(contact["name"])
        ids[contact["name"]] = (
            existing["id"] if existing else req("/api/contacts", contact)["contact"]["id"]
        )

    by_role = {
        "orchestrator": ids["阿核"],
        "writer": ids["文澜"],
        "designer": ids["制图"],
        "generalist": ids["数擎"],
    }
    all_members = list(ids.values())
    # Plain-language member roles — what a teammate does, no internal jargon.
    GROUP_ROLES = {
        by_role["orchestrator"]: "帮你理清需求、安排分工、把成果拼到一起",
        by_role["writer"]: "写文档 / 文案 / 报告",
        by_role["designer"]: "做网页 / 界面 / 视觉",
        by_role["generalist"]: "数据 / 表格 / 脚本 / 分析",
    }
    existing_workspaces = {w["name"]: w for w in get("/api/workspaces")}
    existing_convs = {
        (c.get("workspace_id"), c["title"]): c for c in get("/api/conversations")
    }
    manifest = []
    colors = ["#D97757", "#5AA17F", "#5C7EC7", "#B66A4B", "#8A64D8", "#C0914A"]

    for idx, (key, title, who, cat, spec, task, behavior) in enumerate(CASES):
        if who == "group":
            orch = by_role["orchestrator"]
            members = ["you"] + all_members
            member_roles = GROUP_ROLES
            desc = title
        else:
            role = who.split(":", 1)[1]
            agent = by_role[role]
            members = ["you", agent]
            member_roles = {agent: "直接帮你把这件事做出来"}
            desc = title

        ws_row = existing_workspaces.get(title)
        if ws_row:
            workspace_id = ws_row["id"]
        else:
            workspace_id = req(
                "/api/workspaces",
                {"name": title, "desc": desc, "members": members,
                 "color": colors[idx % len(colors)]},
            )["workspace"]["id"]
            existing_workspaces[title] = {"id": workspace_id}

        existing_conv = existing_convs.get((workspace_id, title))
        if who == "group":
            conv = existing_conv or req(
                "/api/conversations",
                {"workspace_id": workspace_id, "title": title, "members": members,
                 "group": True, "direct": False, "member_roles": member_roles,
                 "orchestrator_member_id": orch, "draft_text": task},
            )
        else:
            conv = existing_conv or req(
                "/api/conversations",
                {"workspace_id": workspace_id, "title": title, "members": members,
                 "group": False, "direct": True, "member_roles": member_roles,
                 "draft_text": task},
            )

        if existing_conv:
            req(f"/api/conversations/{conv['id']}/draft", {"draft_text": task},
                method="PATCH")
        attachments = sample_attachments(key, conv["id"])
        if attachments:
            req(f"/api/conversations/{conv['id']}/draft_attachments",
                {"draft_attachments": attachments}, method="PATCH")
        manifest.append({"key": key, "title": title, "who": who, "cat": cat,
                         "spec": spec, "behavior": behavior, "attachments": len(attachments)})

    _print_stats(manifest)
    return 0


