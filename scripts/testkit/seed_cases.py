def _bar(n: int, total: int, width: int = 24) -> str:
    fill = 0 if total == 0 else round(width * n / total)
    return "█" * fill + "·" * (width - fill)


def _print_stats(manifest: list[dict]) -> None:
    total = len(manifest)
    groups = sum(1 for m in manifest if m["who"] == "group")
    solos = total - groups
    with_attach = [m for m in manifest if m["attachments"]]
    spec_c = Counter(m["spec"] for m in manifest)
    cat_c = Counter(m["cat"] for m in manifest)
    beh_c = Counter(m["behavior"] for m in manifest if m.get("behavior"))

    print()
    print("┏━━ 用例统计(站在用户视角:大多是效果驱动、没想好产物的)━━━━━━━━━━")
    print(f"┃ 总用例     {total}  ·  群聊 {groups} / 单聊 {solos}  ·  带附件 {len(with_attach)}")
    print("┃")
    print("┃ 按「用户清晰度」:")
    for spec in ("vague", "medium", "specific"):
        n = spec_c.get(spec, 0)
        pct = 0 if total == 0 else round(100 * n / total)
        print(f"┃   {_SPEC_LABEL[spec]:<22} {n:>2} {_bar(n, total)} {pct:>3}%")
    print("┃")
    print("┃ 按领域:")
    for cat, n in cat_c.most_common():
        print(f"┃   {_CAT_LABEL.get(cat, cat):<12} {n:>2} {_bar(n, total, 16)}")
    if beh_c:
        print("┃")
        print("┃ 系统能力探针(用例专门测这些行为):")
        for beh, n in beh_c.most_common():
            print(f"┃   {_BEH_LABEL.get(beh, beh):<18} {n:>2} {_bar(n, total, 14)}")
    if with_attach:
        print("┃")
        print(f"┃ 上传驱动的用例:{'、'.join(m['title'] for m in with_attach)}")
    print("┗━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━")

    # Live counts from the API (authoritative).
    try:
        workspaces = get("/api/workspaces")
        convs = get("/api/conversations")
        agents = [a for a in get("/api/agents") if a.get("custom")]
        print(f"✓ 已就绪 —— {len(workspaces)} 个工作区 · {len(convs)} 个会话 · {len(agents)} 个 agent")
        for a in sorted(agents, key=lambda x: x.get("name", "")):
            s = a.get("setup") or {}
            print(f"  {a.get('name')}: {s.get('adapter_id')}/{s.get('model')}")
    except urllib.error.URLError:
        pass


